        public override string[] GetRolesForUser(string userEmail)
        {
            using (ELearningDBContext context = new ELearningDBContext())
            {
                var userRoles = context.Users
                    .Include("UserRole")
                    .Where(w => w.Email.ToLower() == userEmail.ToLower())
                    .Select(c => c.UserRole.Role).ToArray();
                return userRoles;
            }
